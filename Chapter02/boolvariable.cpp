#include <iostream>

using namespace std;

int main()
{
    bool isRaining = false;

    isRaining = true;
    cout << "Is it raining? " << isRaining << endl;

    isRaining = false;
    cout << "Is it raining still? " << isRaining;

    return 0;
}
